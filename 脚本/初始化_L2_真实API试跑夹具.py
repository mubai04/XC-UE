def main() -> None:
    EXPECTED_DIR.mkdir(parents=True, exist_ok=True)
    for cid, body in CHAPTERS.items():
        cdir = CASES / cid
        (cdir / "chapters").mkdir(parents=True, exist_ok=True)
        need_ir = cid in IR_FILES
        seq = ["chapters/prior.md", "chapters/chapter.md"] if cid in PRIOR else None
        (cdir / "project.json").write_text(
            json.dumps(proj(cid, sequence=seq, need_ir=need_ir), ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        full_body = body.strip()
        if cid in CHAPTER_PAD:
            full_body = full_body + "\n\n" + CHAPTER_PAD[cid].strip()
        (cdir / "chapters" / "chapter.md").write_text(full_body + "\n", encoding="utf-8")
        if cid in PRIOR:
            (cdir / "chapters" / "prior.md").write_text(PRIOR[cid].strip() + "\n", encoding="utf-8")
        if cid in IR_FILES:
            (cdir / "IR").mkdir(exist_ok=True)
            for name, content in IR_FILES[cid].items():
                (cdir / "IR" / name).write_text(content.strip() + "\n", encoding="utf-8")
        mod, ft, dr, quote = FT_MAP[cid]
        (cdir / "failure_item.json").write_text(
            json.dumps(failure(cid, mod, ft, quote, dr), ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        (EXPECTED_DIR / f"{cid}.expected.json").write_text(
            json.dumps(EXPECTED[cid], ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
    print(f"OK: {len(CHAPTERS)} cases at {ROOT}")


if __name__ == "__main__":
    main()
