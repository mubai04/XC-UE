from __future__ import annotations

FULL_CHAPTER_RULES = """
全章判断规则（适用于全部六个维度）：
1. 结论必须基于整章表现，不得因单个清晰段落、单句对话或局部亮点直接判 PASS。
2. 必须通读开头、中段、结尾后再下结论。
3. 判 PASS 前必须主动检查全章是否存在：重复、主体切换不清、无效支线、多次近似收尾、核心兑现后继续空转。
4. analysis_summary 必须同时说明：主要优点、主要风险、为何最终选择 PASS / REVIEW / FAIL。
5. evidence 只是代表性锚点，不得用单条摘句证明整章结论。
6. final_reason 必须引用具体全章现象类别（如重复、主体切换、事件增量、支线、规则兑现、章末承接），不得只写“情节清晰”“信息丰富”“容易理解”等空泛语。
7. 因果维度若判 PASS，final_reason 必须明确写出完整链条：起因 → 行动 → 结果，缺一项不得 PASS。
8. 章末追读维度的 evidence 必须来自当章最后 20% 段落。
"""

DIMENSION_RUBRICS: dict[str, dict[str, str]] = {
    "因果": {
        "PASS": "全章主要事件链条连续：起因、行动、结果可追踪；局部跳跃不破坏主线因果。",
        "REVIEW": "主线因果仍可辨认，但存在断点、跳切或结果延后，需要读者补全。",
        "FAIL": "事件顺序持续混乱，行动与结果 repeatedly 对不上，或关键因果链缺失。",
    },
    "动机": {
        "PASS": "主要人物目标、压力与选择在全章保持一致且可理解。",
        "REVIEW": "动机大体成立，但中段出现目标漂移、压力来源不清或选择理由不足。",
        "FAIL": "人物为何行动持续不明，或行为与既定压力/目标反复矛盾。",
    },
    "冲突": {
        "PASS": "全章存在清晰、持续的阻力、对抗或未解问题，并推动段落推进。",
        "REVIEW": "冲突存在但强度波动大，或中后段被重复内容稀释。",
        "FAIL": "表面有对抗语汇，但全章缺少有效阻力，或冲突被重复空转替代。",
    },
    "读者收益": {
        "PASS": "全章持续提供新的事件变化、人物认识、情绪兑现、局势变化或有效悬念；中后段未大面积重复已兑现内容。",
        "REVIEW": "核心事件与主要兑现成立，但中后段重复、空转或信息增量下降，阅读效率不足。",
        "FAIL": "核心机制或爽点完成后，较长篇幅重复相同规则、笑点、反馈或问答；新增信息不足，重复稀释主线或章末钩子。",
    },
    "认知成本": {
        "PASS": "人物、对话主体、场景和信息层级稳定可辨；专名或规则存在但不妨碍连续理解；重复较少。",
        "REVIEW": "主线仍可理解，但存在明显主体切换不清、短句跳转、规则重复、载体频繁切换或局部回读需求，已影响流畅度。",
        "FAIL": "主体、对话、场景或事件顺序持续不清；规则、评论、系统信息、支线或近似内容反复叠加；读者需多次回读才能确认谁在说话或事件是否结束；问题贯穿全章。",
    },
    "章末追读": {
        "PASS": "章末形成明确的新问题、未竟后果或下一章理由，且与前文推进自然衔接。",
        "REVIEW": "章末有承接意图，但钩子力度不足，或与前文重复、收束过早。",
        "FAIL": "章末缺乏有效新问题/新变量，或多次近似收尾导致追读动机不足。",
    },
}


def 格式化标尺文本() -> str:
    lines = [FULL_CHAPTER_RULES.strip(), "", "六维三级标尺："]
    for name, levels in DIMENSION_RUBRICS.items():
        lines.append(f"【{name}】")
        for level in ("PASS", "REVIEW", "FAIL"):
            lines.append(f"- {level}：{levels[level]}")
        lines.append("")
    return "\n".join(lines).strip()
